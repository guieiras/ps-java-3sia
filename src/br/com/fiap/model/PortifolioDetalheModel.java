package br.com.fiap.model;

public class PortifolioDetalheModel {

	
	private int idPortifolio;
	private int idPortifolioDetalhe;
	private String descricaoDetalhe;
	
	
	public int getIdPortifolio() {
		return idPortifolio;
	}
	public void setIdPortifolio(int idPortifolio) {
		this.idPortifolio = idPortifolio;
	}
	public int getIdPortifolioDetalhe() {
		return idPortifolioDetalhe;
	}
	public void setIdPortifolioDetalhe(int idPortifolioDetalhe) {
		this.idPortifolioDetalhe = idPortifolioDetalhe;
	}
	public String getDescricaoDetalhe() {
		return descricaoDetalhe;
	}
	public void setDescricaoDetalhe(String descricaoDetalhe) {
		this.descricaoDetalhe = descricaoDetalhe;
	}
	
	
	
}

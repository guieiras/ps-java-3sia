package br.com.fiap.model;

public class PortifolioFotosModel {

	
	private int idPortifolio;
	private int idPortifolioFoto;
	private String urlFoto;
	private String descricaoFoto;
	
	public int getIdPortifolio() {
		return idPortifolio;
	}
	public void setIdPortifolio(int idPortifolio) {
		this.idPortifolio = idPortifolio;
	}
	public int getIdPortifolioFoto() {
		return idPortifolioFoto;
	}
	public void setIdPortifolioFoto(int idPortifolioFoto) {
		this.idPortifolioFoto = idPortifolioFoto;
	}
	public String getUrlFoto() {
		return urlFoto;
	}
	public void setUrlFoto(String urlFoto) {
		this.urlFoto = urlFoto;
	}
	public String getDescricaoFoto() {
		return descricaoFoto;
	}
	public void setDescricaoFoto(String descricaoFoto) {
		this.descricaoFoto = descricaoFoto;
	}
	
	
	
}
